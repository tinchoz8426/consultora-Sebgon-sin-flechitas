<h3>Sistema de Autoprotección de la ley 5920</h3>



<p><b>Algunos puntos a tener en cuenta sobre esta ley 5920 de Autoprotección</b><br>

<b>¿En qué consiste el Sistema de Autoprotección de la ley 5920?</b><br>
El Sistema de Autoprotección consiste en un conjunto de acciones y medidas destinadas a prevenir y controlar los riesgos sobre las personas y los bienes, para proporcionar una respuesta adecuada a las posibles situaciones de emergencia que se puedan dar en el establecimiento.<br>

<b>¿Quiénes están obligados a disponer de un Sistema de Autoprotección Ley 5920?</b><br>
La obligación  depende de una serie de requisitos establecidos en la Disposición 1358 DGDCIV 2018, algunos de los cuales son: <br><br>
<li>Actividad para la que está habilitado el local.</li><li>
Metros cuadrados.</li><li>
Número de plantas.</li><br>

Conforme al siguiente resumen, los locales del Grupo 1 estarían exentos, debiendo presentar únicamente una Declaración Jurada.<br>

Los establecimientos encuadrados en el Grupo 2 y 3 si están obligados a disponer de un Sistema de Autoprotección Ley 5920 aprobado por Defensa Civil.<br>

Mediante la Disposición 1358/DGDC del 2018 se definieron los parámetros para la clasificación de los establecimientos según criterios de riesgos.<br>



Se trata de  tres grupos:<br><br>
<ol>
<li>
Grupo de Complejidad de evacuación  “BAJA”</li><li>
Grupo de Complejidad de evacuación  “MEDIA”</li><li>
Grupo de Complejidad de evacuación  “ALTA”</li>
</ol>
<b>GRUPO 1:</b> Básicamente son establecimientos de menos de 200 m2 cubiertos, en una planta y que no cuenten con materiales explosivos o inflamables.<br><br>

<b>GRUPO 2:</b> Son aquellos establecimientos de planta baja y hasta un máximo de un (1) piso elevado o cuya superficie cubierta no supere los 1000 m2 o cuando cuenten con líquidos inflamables hasta un máximo de 200 litros.<br><br>

<b>Grupo 3:</b> Establecimientos de dos (2) pisos o más cuando su superficie cubierta sea superior a 1000 m2 o cuando se desarrolle en subsuelos o cuando empleen, expendan, utilicen como parte deprocesos, más de 200 litros de sustancias líquidas inflamables o sustancias químicas peligrosas.<br><br>

En estos casos las  industrias del GRUPO 3 deberán formar BRIGADA DE LUCHA CONTRA INCENDIOS.<br>

Las brigadas de lucha contra incendios será capacitada por personal que acredite experiencia en el Cuerpo de Bomberos de la Ciudad de Buenos Aires u otros cuerpos de bomberos profesionales ( Policía Federal, Prefectura, Policía de la Provincia de Buenos Aires) y Bomberos Voluntarios habilitados por la Dirección General de Defensa Civil.<br>
<b>También en algunas actividades de GRUPO 3 se requiere una evaluación mediante un programa informático de dinámica de humo y evolución del fuego</b>(establecimientos comerciales, sanitarios, educativos, de espectáculos, etc).<br>
A partir del 02 de mayo todas las industrias alcanzadas por esta Disposicióndeberán presentar su correspondiente SISTEMA DE AUTOPROTECCION que será firmado solamente por Profesionales habilitados por el “Registro de Profesionales para la elaboración y puesta a prueba de los Sistemas de Autoprotección”.<br><br>
