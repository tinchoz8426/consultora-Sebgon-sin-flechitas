							<div class="content">
   <p>La Superintendencia de Riesgos del Trabajo ha publicado una nueva  resolución con vigencia a partir del 1º de agosto, en la cual establece  una serie de cambios referidos a la suscripción y al contrato de  afiliación.</p>
   <p>En primer lugar, la norma dispone la  implementación de un nuevo modelo de solicitud de afiliación y del  contrato tipo de afiliación. Asimismo establece la obligatoriedad, por  parte del empleador, de declarar al momento de la suscripción de la  solicitud de afiliación o previo a la renovación automática del  contrato, los datos de los establecimientos que posea; el estado de  cumplimiento de las normas de higiene y seguridad laboral de cada uno  de ellos, y el plan de regularización de los incumplimientos allí  consignados.</p>
   <p>La resolución S.R.T. 463/09 modifica la  solicitud de afiliación, el contrato tipo de afiliación y crea el  registro de cumplimiento de normas de salud, higiene y seguridad en el  trabajo.</p>
   <p>La resolución 529/09 sustituye los art. 10, 11, y  20 de la resolución 463/09 y establece que todo contrato con fecha de  inicio de vigencia o que se renueve a partir del 01/08/09 debe cumplir  con este nuevo marco normativo</p>
   <p>Asimismo, las empresas  deberán completar el Relevamiento General de Riesgos Laborales y, en  caso de corresponder, el plan de regularización de incumplimientos  denunciados con frecuencia anual antes de la fecha de renovación de la  vigencia del contrato.</p>
   <p>Recuerde que, según lo establecido  por la norma, en caso de que el empleador no presente la documentación  dentro del plazo establecido, la ART deberá incrementar en un 50% las  alícuotas correspondientes hasta tanto se regularice la situación.  Además la ART deberá denunciar el incumplimiento ante la SRT, y la  empresa no podrá traspasarse a otra ART hasta tanto se normalicen las  condiciones.</p>
   <p>Completar y enviar estos Formularios es  fundamental para la correcta realización de las acciones de prevención  y evitar sanciones por incumplir lo establecido en las resoluciones SRT  463/09 y 529/09.</p>
   <p>En   la <strong>Consultora Sebgon</strong> podemos   ayudarlo cumpliendo con la normativa vigente, evitando sanciones y costos   adicionales.</p>
